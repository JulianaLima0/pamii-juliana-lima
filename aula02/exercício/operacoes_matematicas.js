// SOMA

function soma (a, b) {
    return a + b
}

console.log("O resultado da soma é:",soma(2, 23))



// SUBTRAÇÃO

function subtracao (a, b) {
    return a - b
}

console.log("O resultado da subtração é: ",subtracao(16, 8))



// DIVISÃO

function divisao (a, b) {
    return a / b
}

console.log("O resultado da divisão é:",divisao(1258, 4))



// MULTIPLICAÇÃO

function multiplicacao (a, b) {
    return a * b
}

console.log("O resultado da multiplicação é:",multiplicacao(12, 8))


// RADICIAÇÃO

function raiz (a) {
    return Math.sqrt(a) 
}

console.log("A raiz é:",raiz(25))



// POTENCIAÇÃO

function potencia (a, b) {
    return a ** b
}

console.log("A potência é:",potencia(6, 2))